#!/usr/bin/env node
/**
 * Reads each example's relationships.json (line-delimited JSON) and
 * writes app/src/data/offlineExamples.js — a single importable module
 * containing all tuple data for offline mode.
 */
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const examplesDir = join(__dirname, "..", "examples");
const outFile = join(__dirname, "..", "app", "src", "data", "offlineExamples.js");

const examples = readdirSync(examplesDir, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name)
  .sort();

const data = {};

for (const name of examples) {
  const relFile = join(examplesDir, name, "relationships.json");
  try {
    const raw = readFileSync(relFile, "utf-8");
    const tuples = raw
      .split("\n")
      .map((line) => line.trim())
      .filter(Boolean)
      .map((line) => JSON.parse(line));
    data[name] = tuples;
    console.log(`  ${name}: ${tuples.length} tuples`);
  } catch (err) {
    console.warn(`  ${name}: skipped (${err.message})`);
  }
}

const output = `// Auto-generated by scripts/generate-offline-data.mjs — do not edit
const OFFLINE_EXAMPLES = ${JSON.stringify(data, null, 2)};

export default OFFLINE_EXAMPLES;
`;

writeFileSync(outFile, output);
console.log(`\nWrote ${outFile}`);
